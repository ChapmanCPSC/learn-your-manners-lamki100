import Foundation

struct Please : Manner {
    
    var name: String
    var image: String
    var description: String
    
    init()
    {
        self.name = "Saying Please and Thank You"
        self.image = "please.png"
        self.description = "“Please” and “thank you” are both often referred to as “the magic words”. Both terms are polite ways of showing someone that you appreciate something they've done for you.If you'd like someone to hold the door open for you, saying “Please hold the door” sounds like a polite request!\n\nSaying “thank you” does the same thing. If someone helped you look for a lost button or bought you a present, saying “thank you” shows that person that you appreciate the time and thought they have given you in helping to look for the lost button or buying you a present!"
    }
}
