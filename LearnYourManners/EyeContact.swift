import Foundation

struct EyeContact : Manner {

    var name: String
    var image: String
    var description: String
    
    init()
    {
        self.name = "Eye Contact"
        self.image = "eyeContact.jpg"
        self.description = "Talking to a group – When talking to a group of people it is great to have direct contact with your listeners. Don’t make the mistake of maintaining eye contact with just one person as this will stop the other members of the group from listening. To get past this, focus on a different member of the group with every new sentence. This way you are talking to all of the group and keeping them all interested. \n\nTalking to an individual – It is great to maintain eye contact when talking to a person however it can become a bit creepy and uncomfortable if you stare intensely at them. To combat this, break eye contact every 5 seconds or so. When breaking the eye contact don’t look down as this might indicate the ending of your part of the conversation. Instead, look up or to the side as if your are remembering something."
    }
}
